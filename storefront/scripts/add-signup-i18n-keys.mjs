const LOCALES = ["en", "ja", "de", "fr", "es", "it", "ko", "zh-TW", "ru", "ar"];

let added = 0;
let skipped = 0;

for (const locale of LOCALES) {
  const filePath = path.join(MESSAGES_DIR, `${locale}.json`);
  const raw = fs.readFileSync(filePath, "utf8");
  const json = JSON.parse(raw);

  // Place under a new `signup.*` namespace to keep auth/signup keys clean.
  json.signup ??= {};
  for (const [k, v] of Object.entries(SIGNUP_KEYS[locale])) {
    if (json.signup[k] !== undefined) {
      skipped++;
      continue;
    }
    json.signup[k] = v;
    added++;
  }

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2) + "\n", "utf8");
  console.log(`[signup-i18n] ${locale}.json updated`);
}

console.log(`[signup-i18n] done — added ${added} keys, skipped ${skipped} pre-existing`);
