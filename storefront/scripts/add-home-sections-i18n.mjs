const messagesDir = "messages";
let touched = 0;
for (const [locale, sections] of Object.entries(HOME_SECTIONS)) {
  const file = path.join(messagesDir, `${locale}.json`);
  const json = JSON.parse(fs.readFileSync(file, "utf8"));
  json.home_sections = sections;
  fs.writeFileSync(file, JSON.stringify(json, null, 2) + "\n", "utf8");
  console.log(`✓ ${locale}.json: home_sections (${Object.keys(sections).length} keys)`);
  touched++;
}
console.log(`\nTotal: ${touched} locale files updated`);
